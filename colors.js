function formatColorData(colorData) {
  const formattedData = {}
  Object.keys(colorData).forEach(weight => {
    formattedData[weight] = colorData[weight].toUpperCase().trim()
  })
  return formattedData
}

function colorVar(name, weight) {
  const format = varFormat(name, weight)
  return `var(${format})`
}

function varFormat(name, weight) {
  return `--${name}-${weight}`
}

const colors = {}
Object.keys(allColors).forEach(color => {
  allColors[color].forEach((colorData, index) => {
    colors[`${color}-${index + 1}`] = formatColorData(colorData)
  })
})

